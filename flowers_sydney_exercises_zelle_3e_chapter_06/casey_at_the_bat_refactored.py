def title_and_credit():
    print('Casey at the Bat by Ernest Thayer')
    print()


def stanza_1():
    print('The outlook wasn\'t brilliant for the Mudville nine that day;')
    print('the score stood four to two, with but one inning more to play.')
    print('And then when Cooney died at first, and Barrows did the same,')
    print('a sickly silence fell upon the patrons of the game.')
    print()


def stanza_2():
    print('A straggling few got up to go in deep despair. The rest')
    print('clung to that hope which springs eternal in the human breast;')
    print('they thought, if only Casey could get but a whack at that –')
    print('they\'d put up even money, now, with Casey at the bat.')
    print()


def stanza_3():
    print('But Flynn preceded Casey, as did also Jimmy Blake,')
    print('and the former was a lulu and the latter was a cake,')
    print('so upon that stricken multitude grim melancholy sat,')
    print('for there seemed but little chance of Casey\'s getting to the bat.')
    print()


def stanza_4():
    print('But Flynn let drive a single, to the wonderment of all,')
    print('and Blake, the much despised, tore the cover off the ball;')
    print('and when the dust had lifted, and the men saw what had occurred,')
    print('there was Jimmy safe at second and Flynn a-hugging third.')
    print()


def stanza_5():
    print('Then from five thousand throats and more there rose a lusty yell;')
    print('it rumbled through the valley, it rattled in the dell;')
    print('it knocked upon the mountain and recoiled upon the flat,')
    print('for Casey, mighty Casey, was advancing to the bat.')
    print()


def stanza_6():
    print('There was ease in Casey\'s manner as he stepped into his place;')
    print('there was pride in Casey\'s bearing and a smile on Casey\'s face.')
    print('And when, responding to the cheers, he lightly doffed his hat,')
    print('no stranger in the crowd could doubt \'twas Casey at the bat.')
    print()


def stanza_7():
    print('Ten thousand eyes were on him as he rubbed his hands with dirt;')
    print('five thousand tongues applauded when he wiped them on his shirt.')
    print('Then while the writhing pitcher ground the ball into his hip,')
    print('defiance gleamed in Casey\'s eye, a sneer curled Casey\'s lip.')
    print()


def stanza_8():
    print('And now the leather-covered sphere came hurtling through the air,')
    print('and Casey stood a-watching it in haughty grandeur there.')
    print('Close by the sturdy batsman the ball unheeded sped—')
    print('"That ain\'t my style," said Casey. "Strike one," the umpire said.')
    print()


def stanza_9():
    print('From the benches, black with people, there went up a muffled roar,')
    print('like the beating of the storm-waves on a stern and distant shore.')
    print('"Kill him! Kill the umpire!" shouted someone on the stand;')
    print('and it\'s likely they\'d have killed him had not Casey raised his hand.')
    print()


def stanza_10():
    print('With a smile of Christian charity great Casey\'s visage shone;')
    print('he stilled the rising tumult; he bade the game go on;')
    print('he signaled to the pitcher, and once more the spheroid flew;')
    print('but Casey still ignored it, and the umpire said: "Strike two."')
    print()


def stanza_11():
    print('"Fraud!" cried the maddened thousands, and Echo answered fraud;')
    print('but one scornful look from Casey and the audience was awed.')
    print('They saw his face grow stern and cold, they saw his muscles strain,')
    print('and they knew that Casey wouldn\'t let that ball go by again.')
    print()


def stanza_12():
    print('The sneer is gone from Casey\'s lip, his teeth are clenched in hate;')
    print('he pounds with cruel violence his bat upon the plate.')
    print('And now the pitcher holds the ball, and now he lets it go,')
    print('and now the air is shattered by the force of Casey\'s blow.')
    print()


def stanza_13():
    print('Oh, somewhere in this favored land the sun is shining bright;')
    print('the band is playing somewhere, and somewhere hearts are light,')
    print('and somewhere men are laughing, and somewhere children shout;')
    print('but there is no joy in Mudville — mighty Casey has struck out.')


