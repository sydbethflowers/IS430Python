# print_dickinson
# Print an Emily Dickinson poem using a multi-function program


def main():
    title_author()
    stanza1()
    stanza2()
    stanza3()
    stanza4()


def title_author():
    print('THE RAILWAY TRAIN by Emily Dickinson\n')


def stanza1():
    print('I like to see it lap the miles,')
    print('And lick the valleys up,')
    print('And stop to feed itself at tanks;')
    print('And then, prodigious, step\n')


def stanza2():
    print('Around a pile of mountains,')
    print('And, supercilious, peer')
    print('In shanties by the sides of roads;')
    print('And then a quarry pare\n')


def stanza3():
    print('To fit its sides, and crawl between,')
    print('Complaining all the while')
    print('In horrid, hooting stanza;')
    print('Then chase itself down hill\n')


def stanza4():
    print('And neigh like Boanerges;')
    print('Then, punctual as a star,')
    print('Stop — docile and omnipotent —')
    print('At its own stable door.')


main()
